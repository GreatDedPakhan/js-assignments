/**
 * Returns the string representation of rectangle with specified width and height
 * using pseudograhic chars
 *
 * @param {number} width
 * @param {number} height
 * @return {string}
 *
 * @example
 *
 *            '┌────┐\n'+
 *  (6,4) =>  '│    │\n'+
 *            '│    │\n'+
 *            '└────┘\n'
 *
 *  (2,2) =>  '┌┐\n'+
 *            '└┘\n'
 *
 *             '┌──────────┐\n'+
 *  (12,3) =>  '│          │\n'+
 *             '└──────────┘\n'
 *
 */
function getRectangleString(width, height) {
    var top_left = '┌';
    var top_right = '┐\n';
    var bottom_left = '└';
    var bottom_right = '┘\n';
    var body_left = '│';
    var body_right = '│\n';
    var up_line = '';
    var body_line = '';
    var result = '';

    if (width >= 2 && height >= 2) {
        for (var i = 0; i < width - 2; i++) {
            up_line += '─';
            body_line += ' ';
        }
        top_left += up_line + top_right;
        body_left += body_line + body_right;
        bottom_left += up_line + bottom_right;
        result += top_left;
        for (var i = 0; i < height - 2; i++) {
            result += body_left;
        }

        result += bottom_left;
    } 
    return result;
    throw new Error('Not implemented');
}


/**
 * Encode specified string with ROT13 cipher
 * See details:  https://en.wikipedia.org/wiki/ROT13
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *
 *   'hello' => 'uryyb'
 *   'Why did the chicken cross the road?' => 'Jul qvq gur puvpxra pebff gur ebnq?'
 *   'Gb trg gb gur bgure fvqr!' => 'To get to the other side!'
 *   'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz' => 'NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm'
 *
 */
function encodeToRot13(str) {
    var alf1="ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
    var alf2="NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm"
    var new_str="";
    for(var i=0;i<str.length;i++){
       if(alf1.indexOf(str[i])!=-1){
           new_str+=alf2[alf1.indexOf(str[i])];
       }
       else{
           new_str+=str[i]
       }
    }
    return new_str;
    throw new Error('Not implemented');
}

/**
 * Returns true if the value is string; otherwise false.
 * @param {string} value
 * @return {boolean}
 *
 * @example
 *   isString() => false
 *   isString(null) => false
 *   isString([]) => false
 *   isString({}) => false
 *   isString('test') => true
 *   isString(new String('test')) => true
 */
function isString(value) {
    if(value == NaN || value == undefined){
        return false;
    }
    if(typeof value[0] == "string"){
        return true;
    }
    else{
        return false;
    }    
    throw new Error('Not implemented');
}


